const validatePath = (path) => {

  let numHitTrees = 0;
  let j = -3;

  for (let i = 0; i < path.length; i++) {
    // Start point i = 0, j = 0
    if (j + 3 >= path[i][0].length) {
      j -= 28
    } else {
      j += 3;
    }

    if (path[i][0][j] === '#') {
      numHitTrees++;
    }
  }

  return numHitTrees;
}

